    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        ProfesorViaje tmp=new ProfesorViaje();
        if(modeloTabla.getDatos().size()>0 && modeloTablaViaje.getDatos().size()>0){
            int filaUno=this.jTable1.getSelectedRow();
            int filaDos=this.jTable2.getSelectedRow();
            if(filaUno!=-1 && filaDos!=-1){
                Profesor prof=modeloTabla.getDatos().get(filaUno);
                ViajeAsignar viaje=modeloTablaViaje.getDatos().get(filaDos);
                tmp.setProfesor(prof);
                tmp.setViaje(viaje);
                this.modeloTablaAsignar.getDatos().add(tmp);
                this.modeloTablaAsignar.fireTableDataChanged();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // TODO add your handling code here:
            AsignarViajeDAO con=new AsignarViajeDAO();
            if(this.modeloTablaAsignar.getDatos().size()>0){
                for(ProfesorViaje dato : this.modeloTablaAsignar.getDatos()){
                    con.almacenarAsignarViaje(dato.getProfesor().getId_Profesor(), dato.getViaje().getId_Viaje());
                    con=new AsignarViajeDAO();
                }
            }
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Error en la BDD");
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Error en la BDD");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

