    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (modeloTabla.getDatos().size()>0){
            Profesor tmp=modeloTabla.getDatos().get(this.jTable1.getSelectedRow());
            this.jTextField1.setText(tmp.getCurp());
            this.jTextField3.setText(tmp.getNombre());
            this.jTextField2.setText(tmp.getAp_Paterno());
            this.jTextField4.setText(tmp.getAp_Materno());
            for (int i = 1; i < modeloDespacho.getSize(); i++) {
               Despacho temp=(Despacho)modeloDespacho.getElementAt(i);
               if(tmp.getId_Despacho()==temp.getId_Despacho()){
                   this.jComboBox1.setSelectedIndex(i);
                   break;
               }
            }   
        }
    }//GEN-LAST:event_jTable1MouseClicked

