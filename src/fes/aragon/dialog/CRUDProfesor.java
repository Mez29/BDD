    private Profesor evaluarCampos(){
        boolean correcto =true;
        String mensajes="";
        String valor=this.jTextField1.getText();
        Profesor datos=new Profesor();
        if (valor==null || valor.isEmpty() || valor.regionMatches(0, " ", 0, 1) || valor.length()>50) {
            mensajes +="Escribe un despacho\n";
            correcto=false;
        }
        if (!correcto) {
            JOptionPane.showMessageDialog(rootPane, mensajes);
            datos=null;
        } else{
            Profesor tmp=null;
            if(this.jTable1.getSelectedRow()!=-1){
                tmp=modeloTabla.getDatos().get(this.jTable1.getSelectedRow());
            }
            if(tmp!=null){
                datos.setId_Profesor(tmp.getId_Profesor());
            }
            datos.setCurp(valor);
            datos.setNombre(this.jTextField3.getText());
            datos.setAp_Paterno(this.jTextField2.getText());
            datos.setAp_Materno(this.jTextField4.getText());
            Despacho tmpDespacho=((Despacho)this.jComboBox1.getSelectedItem());
            datos.setId_Despacho(tmpDespacho.getId_Despacho());
        }
        return datos;
    }
    private void reiniciar(){
        this.jTextField1.setText("");
        this.jTextField2.setText("");
        this.jTextField3.setText("");
        this.jTextField4.setText("");
        this.jComboBox1.setSelectedIndex(0);
    }
