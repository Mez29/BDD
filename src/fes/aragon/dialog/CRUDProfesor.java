    private void IniciarComponentes(){
        try {
            DespachoDAO datos = new DespachoDAO();
            ArrayList<Despacho> despacho=new ArrayList<Despacho>();
            modeloDespacho.addElement("Escoge un despacho");
            despacho=datos.obtenerDespacho();
            for(Despacho des: despacho){
                modeloDespacho.addElement(des);
            }
            ProfesorDAO profesor=new ProfesorDAO();
            modeloTabla.setDatos(profesor.buscarProfesor());
            modeloTabla.setModeloDespacho(modeloDespacho);
            modeloTabla.fireTableDataChanged();
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Error en la BDD");
        } catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Error en la BDD");
        }
    }

